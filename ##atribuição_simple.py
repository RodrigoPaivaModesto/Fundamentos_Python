##atribuição_simple

saldo = 500
print(saldo)

##atribuição_com_adição

saldo += 200
print(saldo)

#usando o '+' antes do '=' faz com que somem as variaveis 'saldo'


## subtração

saldo -= 200
print(saldo)


## multiplicação

saldo *= 2
print(saldo)

## divisão e divisão inteira

saldo //= 2
print(saldo)

saldo /= 1
print(saldo)


## exponenciação

saldo **= 2
print(saldo)


## módulo

saldo %= 25
print(saldo)

